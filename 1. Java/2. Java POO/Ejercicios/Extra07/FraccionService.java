package Extra07;

public class FraccionService {

    public int sumar(int num, int num1) {
        int suma = num + num1;
        return suma;
    }

    public int restar(int num, int num1) {
        int resta = num - num1;
        return resta;
    }

    public int multiplicar(int num, int num1) {
        int mult = num * num1;
        return mult;
    }

    public int dividir(int num, int num1) {
        int div = num / num1;
        return div;
    }

}
